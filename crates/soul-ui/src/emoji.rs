/// Look up the bitmap for `c`. Returns `None` for non-emoji code
/// points (including all of ASCII and Latin-1 — ordinary characters
/// continue to render through the regular font).
pub fn lookup(c: char) -> Option<&'static Glyph> {
    TABLE.iter().find(|(k, _)| *k == c).map(|(_, g)| g)
}

/// Returns true when [`draw_text`] would render `c` from the emoji
/// table rather than from the fallback font.
pub fn is_emoji(c: char) -> bool {
    lookup(c).is_some()
}

/// Width of `c` in monospace cells, used by every layout, cursor,
/// and hit-test calculation in the text widgets. ASCII is 1 cell;
/// emoji are [`EMOJI_CELL_SPAN`].
pub fn cell_width(c: char) -> i32 {
    if is_emoji(c) {
        EMOJI_CELL_SPAN
    } else {
        1
    }
}

/// Sum [`cell_width`] across every char in `s`.
pub fn cells_in(s: &str) -> i32 {
    s.chars().map(cell_width).sum()
}

/// Draw `text` as a monospace run, substituting bitmaps from the
/// emoji table for any code points that have one and deferring to
/// `style.font` for everything else.
///
/// Text characters advance the pen by one font cell; emoji advance
/// by [`EMOJI_CELL_SPAN`] cells. Layout code that asks
/// [`cells_in`] for the width of a given slice will agree with what
/// this function actually paints.
pub fn draw_text<D>(
    canvas: &mut D,
    text: &str,
    pos: Point,
    style: MonoTextStyle<'_, Gray8>,
) -> Result<(), D::Error>
where
    D: DrawTarget<Color = Gray8>,
{
    let cell_w = style.font.character_size.width as i32;
    let cell_h = style.font.character_size.height as i32;
    let color = style.text_color.unwrap_or(super::palette::BLACK);
    let mut x = pos.x;
    let mut buf = [0u8; 4];
    for c in text.chars() {
        if let Some(glyph) = lookup(c) {
            let target = Rectangle::new(
                Point::new(x, pos.y),
                Size::new((cell_w * EMOJI_CELL_SPAN) as u32, cell_h as u32),
            );
            draw_glyph_in_rect_inner(canvas, glyph, target, color)?;
            x += cell_w * EMOJI_CELL_SPAN;
        } else {
            let s = c.encode_utf8(&mut buf);
            Text::with_baseline(s, Point::new(x, pos.y), style, Baseline::Top).draw(canvas)?;
            x += cell_w;
        }
    }
    Ok(())
}

/// Render the glyph for `c` into `target`, scaled and aspect-
/// preserved. Returns `false` (without drawing) if `c` has no entry
/// in the emoji table. Useful for callers that have a generous draw
/// area like an on-screen-keyboard keycap and want emoji at near-
/// native resolution rather than text-cell size.
pub fn draw_glyph_in_rect<D>(
    canvas: &mut D,
    c: char,
    target: Rectangle,
    color: Gray8,
) -> Result<bool, D::Error>
where
    D: DrawTarget<Color = Gray8>,
{
    if let Some(glyph) = lookup(c) {
        draw_glyph_in_rect_inner(canvas, glyph, target, color)?;
        Ok(true)
    } else {
        Ok(false)
    }
}

/// Downsample a 24×24 source bitmap into `target` using a half-area
/// threshold. Aspect ratio is preserved by fitting to the smaller
/// of `target.width` / `target.height` and centring.
fn draw_glyph_in_rect_inner<D>(
    canvas: &mut D,
    glyph: &Glyph,
    target: Rectangle,
    color: Gray8,
) -> Result<(), D::Error>
where
    D: DrawTarget<Color = Gray8>,
{
    let avail_w = target.size.width as i32;
    let avail_h = target.size.height as i32;
    if avail_w <= 0 || avail_h <= 0 {
        return Ok(());
    }
    // Pick the largest square that fits — bitmaps are 24×24, so we
    // preserve aspect by fitting to the smaller dimension.
    let side = avail_w.min(avail_h);
    let off_x = target.top_left.x + (avail_w - side) / 2;
    let off_y = target.top_left.y + (avail_h - side) / 2;
    for oy in 0..side {
        // Source y range covered by this output row.
        let sy0 = (oy * SRC_H) / side;
        let sy1 = (((oy + 1) * SRC_H) / side).max(sy0 + 1);
        for ox in 0..side {
            let sx0 = (ox * SRC_W) / side;
            let sx1 = (((ox + 1) * SRC_W) / side).max(sx0 + 1);
            let mut set = 0;
            let mut total = 0;
            for sy in sy0..sy1 {
                let row = glyph[sy as usize];
                for sx in sx0..sx1 {
                    if (row >> (SRC_W - 1 - sx)) & 1 != 0 {
                        set += 1;
                    }
                    total += 1;
                }
            }
            if set * 2 >= total {
                Pixel(Point::new(off_x + ox, off_y + oy), color).draw(canvas)?;
            }
        }
    }
    Ok(())
}

