    #[test]
    fn cells_in_mixed_string() {
        // "h" + "i" + " " = 3 cells; "♥" = EMOJI_CELL_SPAN cells.
        assert_eq!(cells_in("hi \u{2665}"), 3 + EMOJI_CELL_SPAN);
        assert_eq!(cells_in(""), 0);
    }
