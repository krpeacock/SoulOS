//! Monochrome emoji glyphs for monospace text rendering.
//!
//! SoulOS targets paper-like monochrome and grayscale displays. The
//! modern colour-emoji pipeline (subpixel COLR/CBDT, ZWJ sequences,
//! skin-tone modifiers) does not fit that aesthetic and would dwarf
//! the entire rest of the UI in code size. Instead, this module ships
//! a small fixed table of hand-authored 24×24 monochrome bitmaps —
//! the same sort of marks a user would scrawl in the margin of a
//! paper notebook, just at enough resolution to be legible.
//!
//! ## Authoring
//!
//! Each glyph is a `[u32; 24]`: one row per element, low 24 bits used,
//! bit 23 = leftmost pixel. Designed to be hand-edited in source —
//! one binary literal per row reads as a sketch of the glyph.
//!
//! ## Rendering
//!
//! Glyphs are downsampled at draw time using a half-area threshold
//! (each output pixel is "set" iff at least half the source region it
//! covers is set). That gives crisp results at any target resolution
//! without alpha or anti-aliasing — appropriate for 1-bit panels.
//!
//! In text contexts, an emoji occupies **two** monospace cells of
//! width and one line of height; the bitmap is scaled to fit, with
//! aspect preserved and centred. Each character — emoji or not —
//! advances the layout by [`cell_width`] cells.
//!
//! In larger contexts (e.g. an on-screen-keyboard keycap), callers
//! can render the glyph straight into a target rectangle with
//! [`draw_glyph_in_rect`], which uses the same downsampler and
//! preserves aspect.

